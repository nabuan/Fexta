function stifftext = StiffnessText(p)
m=length(p)-1;
switch m
case 1
stifftext=sprintf ('x(F) =  %0.3eF',p(1) );
case 2
stifftext=sprintf('x(F) = %0.3eF + %0.3eF^2',p(2),p(1)) ;

case 3
stifftext=sprintf('x(F) =  %0.3eF + %0.3eF^2 + %0.3eF^3',p(3),p(2),p(1)) ;

case 4
% x(F) = @(x) p(1).*x.^4  +  p(2).*x.^3  +  p(3).*x.^2  + p(4).*x.^1 ;
stifftext=sprintf('x(F) =  %0.3eF + %0.3eF^2 + %0.3eF^3 + %0.3eF^4',p(4),p(3),p(2),p(1)) ;

case 5
%x(F) = @(x) p(1).*x.^5  +  p(2).*x.^4  +  p(3).*x.^3  + p(4).*x.^2  + p(5).*x.^1 ;
stifftext=sprintf('x(F) =  %0.3eF + %0.3eF^2 + %0.3eF^3 + %0.3eF^4 + %0.3eF^5',p(5),p(4),p(3),p(2),p(1)) ;
case 6
% x(F) = @(x) p(1).*x.^6  +  p(2).*x.^5  +  p(3).*x.^4  +  p(4).*x.^3  +  p(5).*x.^2  + p(6).*x.^1 ;
stifftext=sprintf('x(F) =  %0.3eF + %0.3eF^2 + %0.3eF^3 + %0.3eF^4 + %0.3eF^5 + %0.3eF^6',p(6),p(5),p(4),p(3),p(2),p(1)) ;
case 7
%x(F) = @(x) p(1).*x.^7  +  p(2).*x.^6  +  p(3).*x.^5  +  p(4).*x.^4  +  p(5).*x.^3  + p(6).*x.^2  + p(7).*x.^1 ;
stifftext=sprintf('x(F) =  %0.3eF + %0.3eF^2 + %0.3eF^3 + %0.3eF^4 + %0.3eF^5 + %0.3eF^6 + %0.3eF^7',p(7),p(6),p(5),p(4),p(3),p(2),p(1)) ;
case 8
%x(F) = @(x) p(1).*x.^8  +  p(2).*x.^7  +  p(3).*x.^6  +  p(4).*x.^5  +  p(5).*x.^4  + p(6).*x.^3  + p(7).*x.^2  +  p(8).*x ;    
stifftext=sprintf('x(F) =  %0.3eF + %0.3eF^2 + %0.3eF^3 + %0.3eF^4 + %0.3eF^5 + %0.3eF^6 + %0.3eF^7 + %0.3eF^8',p(8),p(7),p(6),p(5),p(4),p(3),p(2),p(1)) ;
case 9
% x(F) = @(x) p(1).*x.^9  +  p(2).*x.^8  +  p(3).*x.^7  +  p(4).*x.^6  +  p(5).*x.^5  + p(6).*x.^4  + p(7).*x.^3  +  p(8).*x.^2  +  p(9).*x.^1;    
stifftext=sprintf('x(F) =  %0.3eF + %0.3eF^2 + %0.3eF^3 + %0.3eF^4 + %0.3eF^5 + %0.3eF^6 + %0.3eF^7 + %0.3eF^8 + %0.3eF^9',p(9),p(8),p(7),p(6),p(5),p(4),p(3),p(2),p(1)) ;
case 10 
%x(F) = @(x) p(1).*x.^10  +  p(2).*x.^9  +  p(3).*x.^8  +  p(4).*x.^7  +  p(5).*x.^6  + p(6).*x.^5  + p(7).*x.^4  +  p(8).*x.^3  +  p(9).*x.^2 + p(10).*x.^1 ;    
stifftext=sprintf('x(F) =  %0.3eF + %0.3eF^2 + %0.3eF^3 + %0.3eF^4 + %0.3eF^5 + %0.3eF^6 + %0.3eF^7 + %0.3eF^8 + %0.3eF^9 + %0.3eF^10',p(10),p(9),p(8),p(7),p(6),p(5),p(4),p(3),p(2),p(1)) ;
case 11 
%x(F) = @(x) p(1).*x.^11  +  p(2).*x.^10  +  p(3).*x.^9  +  p(4).*x.^8  +  p(5).*x.^7  + p(6).*x.^6  + p(7).*x.^5  +  p(8).*x.^4  +  p(9).*x.^3 + p(10).*x.^2  +   + p(11).*x.^1;    
stifftext=sprintf('x(F) =  %0.3eF + %0.3eF^2 + %0.3eF^3 + %0.3eF^4 + %0.3eF^5 + %0.3eF^6 + %0.3eF^7 + %0.3eF^8 + %0.3eF^9 + %0.3eF^10 + %0.3eF^11',p(11),p(10),p(9),p(8),p(7),p(6),p(5),p(4),p(3),p(2),p(1)) ;
case 12 
%x(F) = @(x) p(1).*x.^12  +  p(2).*x.^11  +  p(3).*x.^10  +  p(4).*x.^9  +  p(5).*x.^8  + p(6).*x.^7  + p(7).*x.^6 +  p(8).*x.^5  +  p(9).*x.^4 +  p(10).*x.^3  +   + p(11).*x.^2 +  + p(12).*x  +  p(13);    
stifftext=sprintf('x(F) =  %0.3eF + %0.3eF^2  +  %0.3eF^3 + %0.3eF^4 + %0.3eF^5 + %0.3eF^6 + %0.3eF^7 + %0.3eF^8 + %0.3eF^9 + %0.3eF^10 + %0.3eF^11 + %0.3eF^12',p(12),p(11),p(10),p(9),p(8),p(7),p(6),p(5),p(4),p(3),p(2),p(1)) ;
end
